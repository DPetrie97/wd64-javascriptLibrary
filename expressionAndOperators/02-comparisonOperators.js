//? Equal
'3' == 3;
console.log("3" == 3);
console.log('bob' == 'Bob'); // false
// == is equal

/**
    - Javascript is helpful and does something known as coercion when comparing values
    - coercion is the process of converting a value from one type to another.
 */


//? Strict Equal 
console.log("3" === 3);
console.log(3 === 3);

// === is strictly equal

console.log('Bob' === 'bob');

//? Not Equal
console.log('3' != 3); // is equal so false
console.log('35' != 3); // not equal so true

//? Strict Not Equal
console.log('3' !== 3);

//? Greater Than
3 > 2;

//? Less Than
2 < 3;

//? Greater Than or Equal To
3 >= 2; // => fat arrow function

//? Less Than or Equal To
2 <= 3;

//? And
"2" && "3";

//? Or
2 || 3;
console.log(2 || 3);